public class switchCase {
    public static void main(String[] args) {
        int num =3;
        switch (num) {
            case 1:
                System.out.println("Time Pass");
                break;
            
            case 2:
                System.out.println("Time Pass");
                break;

            case 3:
                System.out.println("hard Work");
                break;
        
            default:
                System.out.println("Work");
                break;
        }
    }

}
